    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        Selec.add(Terceros);
        Selec.add(Estados);
        Selec.add(Cuentas);
        Selec.add(Entrada);
        if (Terceros.isSelected()){
            
            TercerosOp panerTer = new TercerosOp ();
        panerTer.setSize(700, 500);
        panerTer.setLocation(500, 100);
        PanelFondo.add(panerTer, BorderLayout.CENTER);
        PanelFondo.revalidate();
        PanelFondo.repaint();
  

        
        } else if (Entrada.isSelected()){
             EntradaOp ent1= new EntradaOp();
        ent1.setSize(700, 500);
        ent1.setLocation(500, 100);
        PanelFondo.add(ent1, BorderLayout.CENTER);
        PanelFondo.revalidate();
        PanelFondo.repaint();
        
        
        } else if (Cuentas.isSelected()){
           CuentasOp panerCu = new CuentasOp ();
        panerCu.setSize(700, 500);
        panerCu.setLocation(500, 100);
        PanelFondo.add(panerCu, BorderLayout.CENTER);
        PanelFondo.revalidate();
        PanelFondo.repaint();
        }else if (Estados.isSelected()){
            GenerarOp genCu = new GenerarOp ();
        genCu.setSize(700, 500);
        genCu.setLocation(500, 100);
        PanelFondo.add(genCu, BorderLayout.CENTER);
        PanelFondo.revalidate();
        PanelFondo.repaint();
        } else {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado funci√≥n");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    
